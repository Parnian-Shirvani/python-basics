# Comparison operators

num_1 , num_2 = int(input("Enter 2 numbers : 1. ")) , int(input("2. "))

# == 
print(f"{num_1} == {num_2} : {num_1 == num_2}")

# !=
print(f"{num_1} != {num_2} : {num_1 != num_2}")

# <
print(f"{num_1} < {num_2} : {num_1 < num_2}")

# <=
print(f"{num_1} <= {num_2} : {num_1 <= num_2}")

# >
print(f"{num_1} > {num_2} : {num_1 > num_2}")

# >=
print(f"{num_1} >= {num_2} : {num_1 >= num_2}")
