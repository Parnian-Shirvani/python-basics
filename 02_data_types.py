# Different type of variables

# String 
name = input("What is your name?")
print(type(name))

# Integer number
age = int(input("How old are you?"))
print(type(age))

# Float number
height = float(input("How tall are you?"))
print(type(height))

# Boolean 
is_student = 1 == int(input("Are you a student? (1=yes / 0=no)"))
print(type(is_student))