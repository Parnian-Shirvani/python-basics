# How should we name our variables?
# you should not start your variable names with numbers but they can contain numbers anywhere else 
# you should not use symbols such as : $, @, ^, *, ...
# you can start your variable name with underline _ 
# Python is sensitive to upper or lower case
# in Python, snake_case is the most commonly used naming convention

myName = "Parnian" # Camel case variable naming
MyName = "Parnian" # Upper Camel case variable naming 
myname = "Parnian" # Lower case variable naming
my_name = "Parnian" # Snake_case variable naming
PI = 3.14 # Constants

# Python is a dynamically typed:
# the type of a variable can change during execution

myVariable = "Parnian" # string
myVariable = 22 # integer
myVariable = 159.5 # float